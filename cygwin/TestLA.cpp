    Matrix A;
    Matrix b;
    A.copy(numrow, numrow, matrix4_1);
    b.copy(numrow, 1, vector4_1);
    
    //===========================================================
    A.show();
    b.show();
    
    Matrix ju(numrow, numrow);
    Matrix temp =b;
    for(int k=0; k< numrow; ++k){
        for(int i=0; i< numrow; ++i){
            ju[i][k] = temp[i][0];
        }
        
        temp = A *temp;
    }
    ju.show();
