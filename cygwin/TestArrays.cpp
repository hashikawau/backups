    double* d1 = new double[length];
    double* d2 = new double[length];
    Random r(1);
    for(int i=0; i< length; ++i){
        i1[i] = r.nextInt();
        i2[i] = i1[i];
    }
    r.setSeed(1);
    for(int i=0; i< length; ++i){
        d1[i] = r.nextDouble();
        d2[i] = d1[i];
    }
    
    int t1 = GetTickCount();
    Arrays::sort(d2, length);
    t1 -= GetTickCount();
